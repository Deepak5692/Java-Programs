package looping.Methods;

public class DoWhile {

	public static void main(String[] args) {
		
		
		int use=10;
		
		do {
			
			System.out.println("5");
			
			use++;
		} while (use<=14);
			
		int print=5;
		
		do {
			
			System.out.println("j");
			print++;
			
		} while (print<=9);
	
	int i=10;
	
	do {
		System.out.println("10");
		i++;
	} while (i<=11);
	}
}
