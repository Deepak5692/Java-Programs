package collectionExample;

import java.util.LinkedList;

public class LinkedListExample {

	public static void main(String[] args) {
	
		LinkedList ll=new LinkedList();
		
		//add in linkedlist
		ll.add(20);		
		ll.add(400);
		ll.add(100);
		ll.add("Robin");
		ll.add("Masala");
		
	//	System.out.println(ll);
		
		//Contains to display in list
		ll.containsAll(ll);
		System.out.println(ll);
		
		ll.size();
		System.out.println(ll);
		
		ll.contains(ll);
		System.out.println(ll);
		
		//addfirst in list
		ll.addFirst("Welcome");
		System.out.println(ll);
		
		//addlast in list
		ll.addLast("Exit");
		System.out.println(ll);
		
		ll.getFirst();
		System.out.println("get first "+ll);

		ll.getLast();
		System.out.println(ll);
		
		System.out.println(ll.lastIndexOf(ll));
		
		
		
	}

}
